//Program to Add floating numbers using abstract class
abstract class assal{
   abstract void add_tion();
}
public class summationofloatusingabstract extends assal{
    public void add_tion(){
        float c=00.0456453453f;
        float d=0.0022356f;
        System.out.println(c+d);
    }
    public static void main(String args[])
    {
        summationofloatusingabstract o=new summationofloatusingabstract();
        o.add_tion();
    }
}
